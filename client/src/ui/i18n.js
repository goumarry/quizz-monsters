// Nom générique d'un mini-jeu (roulette) : par défaut, son titre fixe.
for (const dict of Object.values(S)) {
  for (const id of ['intrus', 'chrono', 'spam', 'aires', 'verre', 'feu', 'stroop', 'memo', 'dessine', 'code', 'equation', 'foule']) {
    dict[`mg.${id}.name`] ??= dict[`mg.${id}.title`];
  }
}

let current = detect(false);

function detect(useSdk) {
  const saved = localStorage.getItem('qm-lang');
  if (S[saved]) return saved;
  let locale = '';
  // La locale du SDK n'est fiable que sur le vrai site CrazyGames (en mode
  // « local » elle est factice et écraserait la langue du navigateur).
  if (useSdk) {
    try {
      const sdk = window.CrazyGames?.SDK;
      if (sdk?.environment === 'crazygames') locale = sdk.user?.systemInfo?.locale ?? '';
    } catch { /* no-op */ }
  }
  const code = String(locale || navigator.language || 'en').slice(0, 2).toLowerCase();
  return S[code] ? code : 'en';
}

export function getLang() {
  return current;
}

export function setLang(code) {
  if (!S[code]) return;
  current = code;
  localStorage.setItem('qm-lang', code);
}

// À rappeler après l'init du SDK CrazyGames (sa locale n'est dispo qu'après),
// sans écraser un choix explicite du joueur.
export function autodetect() {
  if (!localStorage.getItem('qm-lang')) current = detect(true);
}

// t('lb.next', { s: 5 }) → « Prochaine manche dans 5s… »
export function t(key, params) {
  let str = S[current][key] ?? S.en[key] ?? key;
  if (params) for (const [k, v] of Object.entries(params)) str = str.replaceAll(`{${k}}`, String(v));
  return str;
}

// Messages venus du serveur : { k: 'res.found', s: '1.2' } → texte localisé.
// Accepte aussi une chaîne brute (rétro-compatibilité).
export function tr(msg) {
  if (msg == null) return '';
  if (typeof msg === 'string') return msg;
  const { k, ...params } = msg;
  return t(k, params);
}

// Nom localisé d'une couleur joueur (Stroop, Mémoire Flash…).
export function colorName(hex) {
  return t(`col.${COLOR_NAMES[hex] ?? ''}`);
}
